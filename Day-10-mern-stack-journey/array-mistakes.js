// Array Mistakes

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. push() vs unshift()

let arr = [2,3,4];

// push → adds at END
arr.push(5);
console.log(arr); // [2,3,4,5]

// unshift → adds at START
arr.unshift(1);
console.log(arr); // [1,2,3,4,5]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake

// Thinking both do same thing

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Wrong Index Access

let nums = [10,20,30];

// Mistake
console.log(nums[3]); // undefined

// Correct
console.log(nums[2]); // 30

// Index always starts from 0

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Modifying Array Incorrectly

let arr2 = [1,2,3];

// Mistake
arr2[5] = 10;
console.log(arr2); // [1,2,3, empty × 2, 10]

// Creates empty slots (bad practice)

// Correct
arr2.push(4);
console.log(arr2); // [1,2,3,4]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Loop Mistake (Very Common)

let arr3 = [5,10,15];

// Mistake
for(let i = 0; i <= arr3.length; i++){
    console.log(arr3[i]); // last will be undefined
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Correct

for(let i = 0; i < arr3.length; i++){
    console.log(arr3[i]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Confusion between Index & Element

let arr4 = [100,200,300];

// Mistake
for(let i = 0; i < arr4.length; i++){
    console.log(i); // prints index
}

// Correct
for(let i = 0; i < arr4.length; i++){
    console.log(arr4[i]); // prints values
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Removing Elements

let arr5 = [1,2,3,4];

// pop → remove last
arr5.pop();

// shift → remove first
arr5.shift();

console.log(arr5); // [2,3]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// Arrays = list of values
// Index = position (starts from 0)
// Logic matters more than syntax

// Small mistakes → wrong output

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------