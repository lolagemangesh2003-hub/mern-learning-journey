// 1️. Add element at start

function addAtStart(arr, value){
    arr.unshift(value);
    return arr;
}

console.log(addAtStart([2,3,4], 1)); // [1,2,3,4]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Add element at end

function addAtEnd(arr, value){
    arr.push(value);
    return arr;
}

console.log(addAtEnd([1,2,3], 4)); // [1,2,3,4]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Get last element

function getLast(arr){
    return arr[arr.length - 1];
}

console.log(getLast([10,20,30])); // 30

