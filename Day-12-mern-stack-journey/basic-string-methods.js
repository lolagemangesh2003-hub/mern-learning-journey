// Strings in JavaScript

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. What is a String?

let str = "Mangesh";
console.log(str);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Length (count characters)

console.log(str.length); // 7

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. toUpperCase() / toLowerCase()

console.log(str.toUpperCase()); // MANGESH
console.log(str.toLowerCase()); // mangesh

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. includes() → check if text exists

let text = "I am learning JavaScript";

console.log(text.includes("Java")); // true
console.log(text.includes("Python")); // false

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. indexOf() → find position

console.log(text.indexOf("learning")); // position number

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6. split() → convert string to array

let data = "a,b,c";

console.log(data.split(",")); // ["a","b","c"]

