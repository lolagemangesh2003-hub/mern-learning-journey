// Star Patterns & Nested Loops

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. What is Nested Loop?

// Loop inside another loop

for(let i = 0; i < 3; i++){
    for(let j = 0; j < 3; j++){
        console.log(i, j);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Key Concept

// Outer Loop --> controls rows
// Inner Loop --> controls columns (what prints in each row)

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Basic Square Pattern

// *
// *
// *

for(let i = 0; i < 3; i++){
    console.log("*");
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Square Pattern (3x3)

// ***
// ***
// ***

for(let i = 0; i < 3; i++){

    let row = "";

    for(let j = 0; j < 3; j++){
        row += "*";
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Right Triangle

// *
// **
// ***

for(let i = 1; i <= 3; i++){

    let row = "";

    for(let j = 1; j <= i; j++){
        row += "*";
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Inverted Triangle

// ***
// **
// *

for(let i = 3; i >= 1; i--){

    let row = "";

    for(let j = 1; j <= i; j++){
        row += "*";
    }

    console.log(row);
}

