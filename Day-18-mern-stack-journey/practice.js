// 1️. Print 4 stars in one row

for(let i = 0; i < 1; i++){
    let row = "";
    for(let j = 0; j < 4; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. 4x4 square

for(let i = 0; i < 4; i++){
    let row = "";
    for(let j = 0; j < 4; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Increasing triangle

for(let i = 1; i <= 4; i++){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Decreasing triangle

for(let i = 4; i >= 1; i--){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += "*";
    }
    console.log(row);
}

