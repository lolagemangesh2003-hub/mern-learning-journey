// 8️. Star + number mix

// *1
// *12
// *123

for(let i = 1; i <= 3; i++){
    let row = "*";
    for(let j = 1; j <= i; j++){
        row += j;
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 9️. Hollow square (basic)

// ****
// *  *
// *  *
// ****

for(let i = 1; i <= 4; i++){
    let row = "";

    for(let j = 1; j <= 4; j++){
        if(i === 1 || i === 4 || j === 1 || j === 4){
            row += "*";
        }else{
            row += " ";
        }
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 10. Pyramid

//  *
// ***
//*****

for(let i = 1; i <= 3; i++){

    let row = "";

    // spaces
    for(let j = 1; j <= 3 - i; j++){
        row += " ";
    }

    // stars
    for(let k = 1; k <= (2*i - 1); k++){
        row += "*";
    }

    console.log(row);
}

