// Pattern Mistakes & Fixes

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. Problem: Star Pattern (Triangle)

// *
// **
// ***

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 1: Wrong Loop Range

for(let i = 1; i <= 3; i++){

    let row = "";

    for(let j = 1; j <= 3; j++){ // always 3
        row += "*";
    }

    console.log(row);
}

// Output:
// ***
// ***
// ***

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Fix: Inner loop depends on row

for(let i = 1; i <= 3; i++){

    let row = "";

    for(let j = 1; j <= i; j++){ // depends on i
        row += "*";
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 2: Not understanding inner loop

// Thinking both loops do same thing

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Understanding

// Outer loop → number of rows
// Inner loop → what prints in that row

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Example: Number Pattern

// 1
// 12
// 123

for(let i = 1; i <= 3; i++){

    let row = "";

    for(let j = 1; j <= i; j++){
        row += j;
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 3: Wrong Pattern Logic

// Wanted:
// *
// **
// ***

// But wrote wrong condition or wrong variable

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Fix: Think per row

// Row 1 → 1 star
// Row 2 → 2 stars
// Row 3 → 3 stars

// Then convert to loop

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Pyramid Pattern (Important)

//   *
//  ***
// *****

for(let i = 1; i <= 3; i++){

    let row = "";

    // spaces
    for(let j = 1; j <= 3 - i; j++){
        row += " ";
    }

    // stars
    for(let k = 1; k <= (2*i - 1); k++){
        row += "*";
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// Don’t memorize patterns
// Don’t guess loops

// Think:
// How many rows?
// What each row prints?
// Convert logic into loop

