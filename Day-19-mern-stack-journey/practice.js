// 1️. Print 5 stars

for(let i = 0; i < 1; i++){
    let row = "";
    for(let j = 0; j < 5; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. 5x5 square

for(let i = 0; i < 5; i++){
    let row = "";
    for(let j = 0; j < 5; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Increasing triangle

for(let i = 1; i <= 5; i++){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Decreasing triangle

for(let i = 5; i >= 1; i--){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += "*";
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Number triangle

for(let i = 1; i <= 5; i++){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += j;
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Same number pattern

for(let i = 1; i <= 5; i++){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += i;
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 7️. Reverse number pattern

for(let i = 5; i >= 1; i--){
    let row = "";
    for(let j = 1; j <= i; j++){
        row += j;
    }
    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 8️. Hollow square

for(let i = 1; i <= 5; i++){
    let row = "";

    for(let j = 1; j <= 5; j++){
        if(i === 1 || i === 5 || j === 1 || j === 5){
            row += "*";
        }else{
            row += " ";
        }
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 9️. Pyramid

for(let i = 1; i <= 5; i++){

    let row = "";

    for(let j = 1; j <= 5 - i; j++){
        row += " ";
    }

    for(let k = 1; k <= (2*i - 1); k++){
        row += "*";
    }

    console.log(row);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 10. Inverted pyramid

for(let i = 5; i >= 1; i--){

    let row = "";

    for(let j = 1; j <= 5 - i; j++){
        row += " ";
    }

    for(let k = 1; k <= (2*i - 1); k++){
        row += "*";
    }

    console.log(row);
}

