// 2D Arrays Basics + Clarity

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. What is 2D Array?

// A 2D array is an array of arrays

let arr = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

console.log(arr);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Rows & Columns

// Rows = outer array
// Columns = inner array values

// Row 0 → [1,2,3]
// Row 1 → [4,5,6]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Access Elements

console.log(arr[0][0]); // 1
console.log(arr[1][2]); // 6
console.log(arr[2][1]); // 8

// Syntax → arr[row][col]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Loop through 2D Array

for(let i = 0; i < arr.length; i++){          // rows

    for(let j = 0; j < arr[i].length; j++){   // columns
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Row-wise Print

for(let i = 0; i < arr.length; i++){
    console.log(arr[i]); // full row
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Column-wise Print

for(let j = 0; j < arr[0].length; j++){

    let col = "";

    for(let i = 0; i < arr.length; i++){
        col += arr[i][j] + " ";
    }

    console.log(col);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Common Mistakes

// 1. Using arr.length for both loops 
// 2. Forgetting arr[i].length 
// 3. Confusing row vs column 

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// 2D Array = Table rows + columns
// Outer loop → rows
// Inner loop → columns

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------