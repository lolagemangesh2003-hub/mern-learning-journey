// 2D Array Practice

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

let arr = [
    [1,2,3],
    [4,5,6],
    [7,8,9]
];

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. Print all elements

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Print row-wise

for(let i = 0; i < arr.length; i++){
    console.log(arr[i]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Print column-wise

for(let j = 0; j < arr[0].length; j++){
    let col = "";
    for(let i = 0; i < arr.length; i++){
        col += arr[i][j] + " ";
    }
    console.log(col);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Find sum of all elements

let sum = 0;

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        sum += arr[i][j];
    }
}

console.log(sum); // 45

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Find max element

let max = arr[0][0];

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        if(arr[i][j] > max){
            max = arr[i][j];
        }
    }
}

console.log(max); // 9

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Find min element

let min = arr[0][0];

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        if(arr[i][j] < min){
            min = arr[i][j];
        }
    }
}

console.log(min); // 1

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 7️. Print diagonal elements

for(let i = 0; i < arr.length; i++){
    console.log(arr[i][i]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 8️. Count even numbers

let count = 0;

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        if(arr[i][j] % 2 === 0){
            count++;
        }
    }
}

console.log(count);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 9️. Convert 2D → 1D array

let result = [];

for(let i = 0; i < arr.length; i++){
    for(let j = 0; j < arr[i].length; j++){
        result.push(arr[i][j]);
    }
}

console.log(result);

