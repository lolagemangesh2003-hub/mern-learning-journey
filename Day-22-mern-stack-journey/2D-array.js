// 2D Array Mistakes & Fixes

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

let arr = [
    [1,2,3],
    [4,5,6],
    [7,8,9]
];

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 1: Confusing rows and columns

for(let i = 0; i < arr[0].length; i++){   //  wrong outer loop

    for(let j = 0; j < arr.length; j++){
        console.log(arr[i][j]); //  wrong access
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Fix

for(let i = 0; i < arr.length; i++){          // rows

    for(let j = 0; j < arr[i].length; j++){   // columns
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 2: Wrong loop limits

for(let i = 0; i <= arr.length; i++){  //  <= causes error

    for(let j = 0; j <= arr[i].length; j++){
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Fix

for(let i = 0; i < arr.length; i++){

    for(let j = 0; j < arr[i].length; j++){
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake 3: Incorrect element access

console.log(arr[i,j]); // wrong

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Fix

console.log(arr[1][2]); // 6

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Column-wise Traversal 

for(let j = 0; j < arr[0].length; j++){

    for(let i = 0; i < arr.length; i++){
        console.log(arr[i][j]);
    }
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Diagonal Elements

for(let i = 0; i < arr.length; i++){
    console.log(arr[i][i]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// Don’t mix row & column
// Don’t use wrong limits

// Row → outer loop
// Column → inner loop
// Access → arr[i][j]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------