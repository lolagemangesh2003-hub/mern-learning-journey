// 1️. Global Scope

let a = 10;

function test1(){
    console.log(a);
}

test1();

// Output : 10
// Global variable can be accessed inside function

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Function Scope

function test2(){
    let b = 20;
    console.log(b);
}

test2();
// console.log(b);

// Output : Error (b is not defined outside function)

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Block Scope

if(true){
    let c = 30;
    console.log(c);
}

// console.log(c);

// Output : Error
// let is block scoped

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. var inside block

if(true){
    var d = 40;
}

console.log(d);

// Output : 40
// var is NOT block scoped

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5. Task :

let x = 50;

function test5(){
    let x = 100;
    console.log(x);
}

test5();
console.log(x);

// Output :
// 100
// 50

// Inner variable overrides outer inside function

