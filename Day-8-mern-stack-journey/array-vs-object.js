// Arrays vs Objects

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1. Arrays :

// What is an Array?

// Array stores multiple values in a single variable

let arr = [1, 2, 3, 4, 5];
console.log(arr);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Accessing Elements (Index starts from 0)

console.log(arr[0]); // 1
console.log(arr[2]); // 3

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Updating Elements

arr[1] = 10;
console.log(arr); // [1,10,3,4,5]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Adding Elements

arr.push(6);     // add at end
arr.unshift(0);  // add at start

console.log(arr);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Removing Elements

arr.pop();      // remove from end
arr.shift();    // remove from start

console.log(arr);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Length of Array

console.log(arr.length);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Loop through Array

for(let i = 0; i < arr.length; i++){
    console.log(arr[i]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Common Methods

let nums = [5, 3, 8, 1];

nums.sort();      // sort (works differently for numbers)
nums.reverse();   // reverse array

console.log(nums);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Convert to String

let str = nums.join("-");
console.log(str); // "1-3-5-8"

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Includes (Check element exists)

console.log(nums.includes(3)); // true
console.log(nums.includes(10)); // false

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// Arrays → store list of values
// Index → position of element
// Loop → used to access all elements

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2. Objects

// What is an Object?

// Object stores data in key-value pairs

let person = {
    name: "Mangesh",
    age: 22,
    role: "Developer"
};

console.log(person);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Accessing Values

console.log(person.name);   // Mangesh
console.log(person["age"]); // 22

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Updating Values

person.age = 23;
console.log(person);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Adding New Property

person.city = "Pune";
console.log(person);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Deleting Property

delete person.role;
console.log(person);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Looping through Object

for(let key in person){
    console.log(key, ":", person[key]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Key Learning

// Arrays → store list of values
// Objects → describe real-world data

// Example:
// Array → ["Mangesh", 22]
// Object → {name: "Mangesh", age: 22}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------