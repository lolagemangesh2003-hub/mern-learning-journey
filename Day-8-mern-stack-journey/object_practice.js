// Object Based Questions

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. Print all keys

let obj1 = {name: "Mangesh", age: 22, role: "Dev"};

for(let key in obj1){
    console.log(key);
}

// Output: name, age, role

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Print all values

let obj2 = {a: 10, b: 20, c: 30};

for(let key in obj2){
    console.log(obj2[key]);
}

// Output: 10, 20, 30

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Count number of properties

let obj3 = {x: 1, y: 2, z: 3};

let count = 0;

for(let key in obj3){
    count++;
}

console.log(count);

// Output: 3

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Check if key exists

let obj4 = {name: "Mangesh", age: 22};

let keyToCheck = "age";

if(keyToCheck in obj4){
    console.log("Key exists");
}else{
    console.log("Key not found");
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Sum of values in object

let obj5 = {a: 10, b: 20, c: 30};

let sum = 0;

for(let key in obj5){
    sum += obj5[key];
}

console.log(sum);

// Output: 60

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Find max value in object

let obj6 = {a: 5, b: 15, c: 10};

let max = -Infinity;

for(let key in obj6){
    if(obj6[key] > max){
        max = obj6[key];
    }
}

console.log(max);

// Output: 15

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 7️. Convert object values to array

let obj7 = {a: 1, b: 2, c: 3};

let arr = [];

for(let key in obj7){
    arr.push(obj7[key]);
}

console.log(arr);

// Output: [1,2,3]

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 8️. Merge two objects

let objA = {a: 1, b: 2};
let objB = {c: 3, d: 4};

let merged = {...objA, ...objB};

console.log(merged);

// Output: {a:1, b:2, c:3, d:4}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 9️. Remove a key

let obj9 = {a: 1, b: 2, c: 3};

delete obj9.b;

console.log(obj9);

// Output: {a:1, c:3}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 10. Find key with specific value

let obj10 = {a: 10, b: 20, c: 30};

let target = 20;

for(let key in obj10){
    if(obj10[key] === target){
        console.log(key);
    }
}

// Output: b

