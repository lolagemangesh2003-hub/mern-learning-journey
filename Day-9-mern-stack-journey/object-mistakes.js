// Object Mistakes

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 1️. Dot vs Bracket Notation

let obj = {
    name: "Mangesh",
    age: 22
};

// Dot notation
console.log(obj.name); // Mangesh

// Bracket notation
console.log(obj["age"]); // 22

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake

let key = "name";
console.log(obj.key); // undefined

// Correct

console.log(obj[key]); // Mangesh

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2️. Accessing Non-Existing Key

console.log(obj.city); // undefined

// Always check before using

if(obj.city){
    console.log("Exists");
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3️. Updating Values

obj.age = 23; // correct
console.log(obj);

// Mistake: using wrong key

obj.Age = 25; // creates new key
console.log(obj);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4️. Adding New Property

obj.role = "Developer";
console.log(obj);

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5️. Nested Objects

let user = {
    name: "Mangesh",
    address: {
        city: "Pune",
        pin: 411027
    }
};

// Access nested value
console.log(user.address.city); // Pune

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Mistake

// console.log(user.city); // undefined

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6️. Looping Object

for(let key in obj){
    console.log(key, ":", obj[key]);
}

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// NOTE :

// 1. Objects = key-value pairs
// 2. Always think → "Which key am I accessing?"

